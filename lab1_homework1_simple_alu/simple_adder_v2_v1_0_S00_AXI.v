    //为了进行计算，构建一个alu，包含四种运算
    alu alu_inst(
	.clk(S_AXI_ACLK),               //把clk传进去，必须，套路
    .resetn(S_AXI_ARESETN),         //reset 也必须传入，套路
    .a(slv_reg0),                   //然后是input1
    .b(slv_reg1),                   //input2
    .command(slv_reg_command),      //传入command
    .data_vld(state_counter == 3'b111),  //传入 是否 state_counter == 3'b111, 其实也可以直接传入 state_counter
    .result(slv_reg3),              //返回给主module：要存结果的reg3
    .result_done(result_done)       //返回给主module：是否完成一个计算循环
	);
